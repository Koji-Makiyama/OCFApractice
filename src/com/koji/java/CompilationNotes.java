package com.koji.java;

public class CompilationNotes {
    /**
     *  If you add throws keyword to a method that throws an exception,
     *  even if code throws an exception,
     *  it will still compile and then exception at runtime.
     *
     *  If you don't add throws keyword to a method that throws an exception,
     *  whether the code throws an exception or not, the class will never compile
     *
     *  TLDR: throws allows you to compile exception triggering code
     */
}
